# реализуем очередь (First In First Out)
#
# методы:
#
# - put (добавить элемент в конец очереди)
# - get (первый элемент "уходит")
# - size (посмотреть, сколько сейчас в очереди элементов)
# - top (посмотреть, какой элемент первый выйдет из очереди)
# - empty (посмотреть, пустая очередь или нет)
# - *capacity (максимальное кол-во элементов в очереди)
# - *full (посмотреть, достигла ли очередь capacity)
#
# очередь реализуем сразу с ограничениями (*)